#include "main.h"
#include <string.h>

/**
 * _strcmp - compares two strings
 * @s1: checker
 * @s2: checker
 *
 * Return: Always 0.
 */
int _strcmp(char *s1, char *s2)
{
	int i, j, num;

	for (i = 0; s1[i] != '\0'; i++)
	{
		for (j = 0; s2[j] != '\0'; j++)
		{
			num = s1 - s2;
		}

	}
	return (num);
}
